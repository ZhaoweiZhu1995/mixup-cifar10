class MNIST(DatasetNumerical):
    resources = [
        ("http://yann.lecun.com/exdb/mnist/train-images-idx3-ubyte.gz", "f68b3c2dcbeaaa9fbdd348bbdeb94873"),
        ("http://yann.lecun.com/exdb/mnist/train-labels-idx1-ubyte.gz", "d53e105ee54ea40749a09fcbcd1e9432"),
        ("http://yann.lecun.com/exdb/mnist/t10k-images-idx3-ubyte.gz", "9fb629c4189551a2d022fa330f9573f3"),
        ("http://yann.lecun.com/exdb/mnist/t10k-labels-idx1-ubyte.gz", "ec29112dd5afa0611ce80d1b7f02629c")
    ]

    training_file = 'training.pt'
    test_file = 'test.pt'
    classes = ['0 - zero', '1 - one', '2 - two', '3 - three', '4 - four',
               '5 - five', '6 - six', '7 - seven', '8 - eight', '9 - nine']

    num_classes = 10

    def __init__(self, root=None, is_train=True, label_file_path=None, transform=None, 
                 selected_idx=None, select_label=True, chosen_classes=None, classes_dist=None, is_download=False):
        self.is_download = is_download
        super(MNIST, self).__init__(root=root, is_train=is_train, label_file_path=label_file_path, transform=transform,
                                    selected_idx=selected_idx, select_label=select_label,
                                    chosen_classes=chosen_classes, classes_dist=classes_dist)

    def load_data(self):
        '''
        MNIST/Fashion MNIST are assumed to be stored in processed train.pt and test.pt files
        '''
        if self.is_download:
            self.download()

        if not self._check_exists():
            raise RuntimeError('Dataset not found.' +
                               ' You can use download=True to download it')

        if self.is_train:
            data_file = self.training_file
        else:
            data_file = self.test_file
        self.data, self.label = torch.load(os.path.join(self.processed_folder, data_file))

        self.data = self.data.unsqueeze(1) #put into N*C*H*W shape
        self.data = self.data.float() / 255. #normalize to 0. ~ 1.

        self.true_label = self.label.clone()

        # load label for noise label or co-training cases
        if (self.label_file_path is not None) and self.is_train:
            self.label = self.load_label()

        assert isinstance(self.data, torch.Tensor)
        assert isinstance(self.label, torch.Tensor)
        assert isinstance(self.true_label, torch.Tensor)
    
    @property
    def raw_folder(self):
        return os.path.join(self.root, self.__class__.__name__, 'raw')

    @property
    def processed_folder(self):
        return os.path.join(self.root, self.__class__.__name__, 'processed')

    @property
    def class_to_idx(self):
        return {_class: i for i, _class in enumerate(self.classes)}
    
    def _check_exists(self):
        return (os.path.exists(os.path.join(self.processed_folder,
                                            self.training_file)) and
                os.path.exists(os.path.join(self.processed_folder,
                                            self.test_file)))

    def download(self):
        """Download the MNIST data if it doesn't exist in processed_folder already."""

        if self._check_exists():
            return

        makedir_exist_ok(self.raw_folder)
        makedir_exist_ok(self.processed_folder)

        # download files
        for url, md5 in self.resources:
            filename = url.rpartition('/')[2]
            download_and_extract_archive(url, download_root=self.raw_folder, filename=filename, md5=md5)

        # process and save as torch files
        print('Processing...')

        training_set = (
            read_image_file(os.path.join(self.raw_folder, 'train-images-idx3-ubyte')),
            read_label_file(os.path.join(self.raw_folder, 'train-labels-idx1-ubyte'))
        )
        test_set = (
            read_image_file(os.path.join(self.raw_folder, 't10k-images-idx3-ubyte')),
            read_label_file(os.path.join(self.raw_folder, 't10k-labels-idx1-ubyte'))
        )
        with open(os.path.join(self.processed_folder, self.training_file), 'wb') as f:
            torch.save(training_set, f)
        with open(os.path.join(self.processed_folder, self.test_file), 'wb') as f:
            torch.save(test_set, f)

        print('Done!')


